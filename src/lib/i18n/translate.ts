export function translateText(locale: Locale, input: string): string {
  if (!input || locale === 'zh-CN') return input;

  const exact = EXACT_TRANSLATIONS[locale][input];
  if (exact) return exact;

  let output = input;
  const replacements = Object.entries(SEGMENT_TRANSLATIONS[locale]).sort((a, b) => b[0].length - a[0].length);
  for (const [source, target] of replacements) {
    output = output.split(source).join(target);
  }

  return output;
}

export function translateMaybe(locale: Locale, input?: string | null): string {
  if (!input) return '';
  return translateText(locale, input);
}

export function localizeUiText(locale: Locale, input: string): string {
  return translateText(locale || DEFAULT_LOCALE, input);
}
